/**
 * True False Table
 *
 * AND (&&)
 * true && true => true
 * true && false => false
 * false && true => false
 * false && false => false
 *
 * OR (||)
 * true || true => true
 * true || false => true
 * false || true => true
 * false || false => false
 */

/**
 * The NOT operator (!)
 * It just reverse a boolean statement
 * NOT true : !true  => false
 * NOT false : !false => true
 * NOT string : !"Hello there" => false
 * NOT empty string : !"" => true
 */

/**
 * And: &&
 * Or: ||
 * Greater: >
 * Lesser: <
 * GreaterOrEqual: >=
 * LesserOrEqual: <=
 *
 * Loose Equality: ==
 * Strict Equality: ===
 * Loose not equal: !=
 * Strict not equal !==
 */

